#  Test-Driven Development!
#  You write your tests before you write the code based on things like acceptance requirements etc.
#  You start by writing and running the tests, then you build the code and keep testing until you pass, then
#  you work on refactoring and organising up your code.

#  RED -> GREEN -> REFACTOR
#  testfail -> testpass -> refactor

#  Unit Testing!
#  Testing specific functions or small areas or units of our code. Testing individual units.
#  i.e lego bricks, you test each brick


